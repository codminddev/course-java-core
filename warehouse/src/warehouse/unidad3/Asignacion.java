package warehouse.unidad3;

public class Asignacion {
	public static void main(String[] args) {
		
		int a = 10;
		int b = 5;
		int c = 15;
		int d = 20;
		int e = 25;
		
		a = 15;
		System.out.println("a = " + a);
		
		b += 5; // b = b + 5;
		System.out.println("b = " + b);
		
		c *= 10; // c = c * 10;
		System.out.println("c = " + c);
		
		d /= 5; // d = d / 5;
		System.out.println("d = " + d);
		
		e %= 6; // e = e % 6
		System.out.println("e = " + e);
	}
}
